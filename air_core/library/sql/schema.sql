
CREATE TABLE CURRENT_WEATHER(
    ID INTEGER PRIMARY KEY AUTOINCREMENT,
    latitude FLOAT NOT NULL,
    longitude FLOAT NOT NULL,
    date TEXT NOT NULL,
    temp_value FLOAT NOT NULL,
    temp_unit TEXT NOT NULL,
    temp_apparent_value FLOAT NOT NULL,
    temp_apparent_unit TEXT NOT NULL,
    moon_phase TEXT NOT NULL,
    humidity_value INTEGER NOT NULL,
    humidity_unit TEXT NOT NULL,
    dew_point_value FLOAT NOT NULL,
    dew_point_unit TEXT NOT NULL,
    weather_code TEXT NOT NULL,
    precipitation_probability_value INTEGER NOT NULL,
    precipitation_probability_unit TEXT NOT NULL,
    precipitation_type TEXT NOT NULL,
    pressure_surface_level_value FLOAT NOT NULL,
    pressure_surface_level_unit TEXT NOT NULL,
    epa_index_value INTEGER NOT NULL,
    epa_index_unit TEXT NOT NULL,
    epa_health_concern TEXT NOT NULL,
    epa_primary_pollutant TEXT NOT NULL,
    particulate_matter10_value FLOAT NOT NULL,
    particulate_matter10_unit TEXT NOT NULL,
    particulate_matter25_value FLOAT NOT NULL,
    particulate_matter25_unit TEXT NOT NULL,
    pollutant_CO_value FLOAT NOT NULL,
    pollutant_CO_unit TEXT NOT NULL,
    pollutant_NO2_value FLOAT NOT NULL,
    pollutant_NO2_unit TEXT NOT NULL,
    pollutant_O3_value FLOAT NOT NULL,
    pollutant_O3_unit TEXT NOT NULL,
    pollutant_SO2_value FLOAT NOT NULL,
    pollutant_SO2_unit TEXT NOT NULL,
    grass_index INTEGER NOT NULL,
    tree_index INTEGER NOT NULL,
    weed_index INTEGER NOT NULL
);

